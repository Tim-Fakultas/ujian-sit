/* eslint-disable @typescript-eslint/no-explicit-any */
import * as React from "react";
import { flexRender } from "@tanstack/react-table";
import {
  Table,
  TableBody,
  TableCell,
  TableHead,
  TableHeader,
  TableRow,
} from "@/components/ui/table";
import { Button } from "@/components/ui/button";

export default function TableGlobal({
  table,
  cols,
}: {
  table: any;
  cols: any[];
}) {
  // Total data (use filtered row model if available so count updates with filters)
  const totalFiltered =
    typeof table.getFilteredRowModel === "function"
      ? table.getFilteredRowModel().rows.length
      : table.getRowModel?.()?.rows.length ?? 0;

  // Total before filter (if available)
  const totalAll =
    typeof table.getPreFilteredRowModel === "function"
      ? table.getPreFilteredRowModel().rows.length
      : totalFiltered;
  return (
    <>
      <div className="overflow-x-auto rounded-md border bg-white dark:bg-neutral-800">
        <Table className="min-w-[700px] sm:min-w-full">
          <TableHeader>
            {table.getHeaderGroups().map((headerGroup: any) => (
              <TableRow key={headerGroup.id}>
                {headerGroup.headers.map((header: any) => {
                  return (
                    <TableHead key={header.id}>
                      {header.isPlaceholder
                        ? null
                        : flexRender(
                            header.column.columnDef.header,
                            header.getContext()
                          )}
                    </TableHead>
                  );
                })}
              </TableRow>
            ))}
          </TableHeader>
          <TableBody>
            {table.getRowModel().rows?.length ? (
              table.getRowModel().rows.map((row: any) => (
                <TableRow
                  key={row.id}
                  data-state={row.getIsSelected() && "selected"}
                >
                  {row.getVisibleCells().map((cell: any) => (
                    <TableCell key={cell.id}>
                      {flexRender(
                        cell.column.columnDef.cell,
                        cell.getContext()
                      )}
                    </TableCell>
                  ))}
                </TableRow>
              ))
            ) : (
              <TableRow>
                <TableCell colSpan={cols.length} className="h-24 text-center">
                  No results.
                </TableCell>
              </TableRow>
            )}
          </TableBody>
        </Table>
      </div>
      <div className="flex items-center justify-end space-x-2 py-4">
        <div className="text-muted-foreground flex-1 text-sm">
          Total data: {totalFiltered}
          {totalAll !== totalFiltered && (
            <span className="text-muted-foreground"> di dari {totalAll}</span>
          )}
        </div>
        <div className="space-x-2">
          <Button
            variant="outline"
            size="sm"
            onClick={() => table.previousPage()}
            disabled={!table.getCanPreviousPage()}
          >
            Previous
          </Button>
          <Button
            variant="outline"
            size="sm"
            onClick={() => table.nextPage()}
            disabled={!table.getCanNextPage()}
          >
            Next
          </Button>
        </div>
      </div>
    </>
  );
}
